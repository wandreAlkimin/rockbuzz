     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = Auth::user();

        // Faz a validação obtendo os dados do modelo representante
        Validator::make($request->all(),$user->rules,$user->messages)->validate();

        $rq = $request->all();

        //Salva os dados
        if(isset($rq['password'])){
            $user->password = Hash::make($rq['password']);
        }
        $user->name  = $rq['name'];
        $user->save();

        Session::flash('msg', 'Atualizado com sucesso');
        return redirect('/adm/profile/');
    }

